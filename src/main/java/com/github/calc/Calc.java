    private void addCalc(java.awt.event.ActionEvent evt) {
        if (initValue) {
            if (evt.getActionCommand().equals(",")) {
                return;
            }
            jTextField1.setText(evt.getActionCommand());
        } else {
            jTextField1.setText(jTextField1.getText() + evt.getActionCommand());
        }
        if (commandCode == '=') {
            savedValue = new BigDecimal(jTextField1.getText().replace(',', '.'));
            currentValue = BigDecimal.ZERO;
        } else {
            currentValue = new BigDecimal(jTextField1.getText().replace(',', '.'));
        }
        initValue = false;
    }

    private void initCalc() {
        currentValue = BigDecimal.ZERO;
        savedValue = BigDecimal.ZERO;
        initValue = true;
        doInitValue = true;
        commandCode = '=';
        jTextField1.setText("0");
    }

    private void fCalc(String command) {
        if("ce".equals(command)) {
            initCalc();
        } else if ("=".equals(command)) {
            if (commandCode != '=' && !initValue) {
                BigDecimal value = new BigDecimal(jTextField1.getText().replace(',', '.'));
                BigDecimal result = BigDecimal.ZERO;
                switch (commandCode) {
                    case '+':
                        result = savedValue.add(value);
                        break;
                    case '-':
                        result = savedValue.subtract(value);
                        break;
                    case '*':
                        result = savedValue.multiply(value);
                        break;
                    case '/':
                        try {
                            result = savedValue.divide(value, 18, BigDecimal.ROUND_HALF_UP);
                        } catch (ArithmeticException ex) {
                            initCalc();
                            jTextField1.setText("Error.");
                            return;
                        }
                        break;
                }
                commandCode = '=';
                jTextField1.setText(result.setScale(16, BigDecimal.ROUND_HALF_UP).toString().replace('.', ',')
                    .replaceFirst("0+$", "").replaceFirst(",$", "").replace("E-16", ""));
                savedValue = result;
                currentValue = BigDecimal.ZERO;
            }
        } else if ("+-".equals(command)) {
                currentValue = new BigDecimal(jTextField1.getText().replace(',', '.'));
                currentValue = currentValue.multiply(new BigDecimal("-1"));
                jTextField1.setText(currentValue.toString().replace('.', ','));
                if (commandCode == '=') {
                    savedValue = currentValue;
                    currentValue = BigDecimal.ZERO;
                }
                doInitValue = true;
        } else if("sqrt".equals(command)) {
                currentValue = new BigDecimal(jTextField1.getText().replace(',', '.'));
                try {
                    currentValue = BigDecimalUtil.sqrt(currentValue);
                } catch (ArithmeticException ex) {
                    ex.getMessage();
                }
                jTextField1.setText(currentValue.setScale(16, BigDecimal.ROUND_HALF_UP).toString().replace('.', ',')
                    .replaceFirst("(.+?)0+$", "$1").replaceFirst(",$", ""));
                if (commandCode == '=') {
                    savedValue = currentValue;
                    currentValue = BigDecimal.ZERO;
                }
                doInitValue = true;
        } else if ("nbs".equals(command)) {
            if (!initValue && jTextField1.getText().matches("[\\d,]+")) {
                BigDecimal value = new BigDecimal(jTextField1.getText().replace(',', '.'));
                if (value.doubleValue() < 10 && value.doubleValue() > -10) {
                    jTextField1.setText("0");
                    initValue = true;
                } else {
                    jTextField1.setText(jTextField1.getText().substring(0, jTextField1.getText().length() - 1));
                }
                return;
            }
        } else if ("+".equals(command)) {
            if (commandCode != '=' && !initValue) {
                BigDecimal value = new BigDecimal(jTextField1.getText().replace(',', '.'));
                BigDecimal result = savedValue.add(value);
                jTextField1.setText(result.toString().replace('.', ','));
                savedValue = result;
                currentValue = BigDecimal.ZERO;
            }
            commandCode = '+';
        } else if ("-".equals(command)) {
            if (commandCode != '=' && !initValue) {
                BigDecimal value = new BigDecimal(jTextField1.getText().replace(',', '.'));
                BigDecimal result = savedValue.subtract(value);
                jTextField1.setText(result.toString().replace('.', ','));
                savedValue = result;
                currentValue = BigDecimal.ZERO;
            }
            commandCode = '-';
        } else if ("*".equals(command)) {
            if (commandCode != '=' && !initValue) {
                BigDecimal value = new BigDecimal(jTextField1.getText().replace(',', '.'));
                BigDecimal result = savedValue.multiply(value);
                jTextField1.setText(result.toString().replace('.', ','));
                savedValue = result;
                currentValue = BigDecimal.ZERO;
            }
            commandCode = '*';
        } else if ("/".equals(command)) {
            if (commandCode != '=' && !initValue) {
                BigDecimal value = new BigDecimal(jTextField1.getText().replace(',', '.'));
                BigDecimal result = savedValue.divide(value, 18, BigDecimal.ROUND_HALF_UP);
                jTextField1.setText(result.setScale(16, BigDecimal.ROUND_HALF_UP).toString().replace('.', ',')
                    .replaceFirst("(.+?)0+$", "$1").replaceFirst(",$", ""));
                savedValue = result;
                currentValue = BigDecimal.ZERO;
            }
            commandCode = '/';
        } else if ("1/x".equals(command)) {
            currentValue = savedValue == BigDecimal.ZERO
                ? new BigDecimal(jTextField1.getText().replace(',', '.')) : savedValue;
            try {
                currentValue = BigDecimal.ONE.divide(currentValue, 18, BigDecimal.ROUND_HALF_UP);
            } catch (ArithmeticException ex) {
                ex.getMessage();
            }
            jTextField1.setText(currentValue.setScale(16, BigDecimal.ROUND_HALF_UP).toString().replace('.', ',')
                .replaceFirst("(.+?)0+$", "$1").replaceFirst(",$", ""));
            if (commandCode == '=') {
                savedValue = currentValue;
                currentValue = BigDecimal.ZERO;
            }
            doInitValue = true;
        } else if ("%".equals(command)) {
            if (commandCode != '=' && !initValue) {
                BigDecimal value = new BigDecimal(jTextField1.getText().replace(',', '.'));
                BigDecimal result = savedValue.multiply(value).divide(BigDecimal.valueOf(100), 18, BigDecimal.ROUND_HALF_UP);
                jTextField1.setText(result.setScale(16, BigDecimal.ROUND_HALF_UP).toString().replace('.', ',')
                    .replaceFirst("(.+?)0+$", "$1").replaceFirst(",$", ""));
                currentValue = result;
                return;
            }
        }
        if (doInitValue) {
            initValue = true;
        } else {
            doInitValue = true;
        }
    }

    private void keyDetect(java.awt.event.ActionEvent evt) {
        if (evt.getActionCommand().charAt(0) >= '0' && evt.getActionCommand().charAt(0) <= '9') {
            addCalc(evt);
        } else if (evt.getActionCommand().charAt(0) == ',' && !jTextField1.getText().contains(",")) {
            addCalc(evt);
        } else if (evt.getActionCommand().charAt(0) == '\u2190') {
            fCalc("nbs");
        }
    }
